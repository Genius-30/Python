a = (1, 2, 3, 4, 5)
print(a)

# Accessing elements of a tuple
print(a[0])

# Slicing a tuple
print(a[1:4])

# Changing a tuple
# Tuples are immutable, so you cannot change the elements of a tuple

# Deleting a tuple
# Tuples are immutable, so you cannot delete the elements of a tuple

# Tuple operations
# Concatenation
b = (6, 7, 8, 9, 10)
print(a + b)

# Repetition
print(a * 2)

# Membership
print(1 in a)

# Iteration
for i in a:
    print(i)

# Built-in functions
# len()
print(len(a))

# max()
print(max(a))

# min()
print(min(a))