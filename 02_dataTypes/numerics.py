# Useful Numeric Functions in Python

# 1. Absolute Value
print("abs(-7):", abs(-7))  # Output: 7

# 2. Round a Number
print("round(3.14159, 2):", round(3.14159, 2))  # Output: 3.14

# 3. Power Calculation
print("pow(2, 3):", pow(2, 3))  # Output: 8

# 4. Sum of Elements
print("sum([1, 2, 3]):", sum([1, 2, 3]))  # Output: 6

# 5. Maximum and Minimum
print("max([1, 2, 3]):", max([1, 2, 3]))  # Output: 3
print("min([1, 2, 3]):", min([1, 2, 3]))  # Output: 1

# 6. Square Root
import math
print("math.sqrt(16):", math.sqrt(16))  # Output: 4.0

# 7. Ceiling and Floor
print("math.ceil(3.4):", math.ceil(3.4))  # Output: 4
print("math.floor(3.4):", math.floor(3.4))  # Output: 3

# 8. Logarithm
print("math.log(8, 2):", math.log(8, 2))  # Output: 3.0

# 9. Random Integer
import random
print("random.randint(1, 10):", random.randint(1, 10))  # Output: Random integer between 1 and 10

# 10. Random Float
print("random.uniform(1.5, 5.5):", random.uniform(1.5, 5.5))  # Output: Random float between 1.5 and 5.5
